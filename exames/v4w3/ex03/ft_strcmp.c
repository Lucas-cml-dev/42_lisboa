#include <stdio.h>
#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	cd;
	
	cd = 0;
	while (s1[cd] != '\0' && (s1[cd] == s2[cd]))
		cd++;
	return (s1[cd] - s2[cd]);	
}