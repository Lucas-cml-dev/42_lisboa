#include <unistd.h>
#include <stdio.h>

int main()
{
	int i;
	char d;
	char u;

	d = '0';
	u = '1';
	i = 1;
	while (i <= 100)
	{
		if (i % 10 == 0)
		{
			u = '0';
			d++;
		}
		if (i % 3 == 0)		
			write(1 , "Fizz", 4);
		if (i % 5 == 0)		
			write(1 , "Buzz", 4);
		if (!(i % 3 == 0 || i % 5 == 0))
		{
			if (i > 10)
			{
				write(1 , &d, 1);
			}
			write(1 , &u, 1);
		}
		
		
		write(1 , "\n", 1);
		i++;
		u++;
	}
	return(0);	
}