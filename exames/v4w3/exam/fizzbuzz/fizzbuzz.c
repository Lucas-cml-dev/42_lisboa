#include <unistd.h>
#include <stdio.h>



int main()
{
	char	d;
	char	u;
	int	i;

	d = '0';
	u = '1';
	i = 1;
	while (i <= 100)
	{
		if (i % 10 == 0)
		{
			d++;
			u = '0';
		}
		if (i % 3 == 0)
			write(1, "fizz", 4);
		if (i % 5 == 0)
			write(1, "buzz", 4);
		if (!(i % 3 == 0 || i % 5 == 0))
		{
			if(i >= 10)
				write(1, &d, 1);
			write(1, &u, 1);
		}
		

		write(1, "\n", 1);
		u++;
		i++;		
	}
	return(0);
}	